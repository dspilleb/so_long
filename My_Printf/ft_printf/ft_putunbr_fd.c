#include "../ft_printf.h"

void	ft_putunbr_fd(unsigned int n, int fd)
{
	if (n < 10)
	{
		ft_putchar_fd((n + '0'), fd);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
